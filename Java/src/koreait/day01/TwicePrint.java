package koreait.day01;

public class TwicePrint {

	public static void main(String[] args) {
		System.out.println("     Twice!     ");
		System.out.println("------------------");
		System.out.println("나연	1995.09.22");
		System.out.println("정연	1996.11.01");
		System.out.println("사나	1996.12.29");
		System.out.println("지효	1997.02.01");
		System.out.println("미나	1997.03.24");
		System.out.printf("%s\t%s","다현","1996.12.29\n");
		System.out.printf("%s\t%s","채영","1996.12.29\n");
		System.out.printf("%s\t%s","쯔위","1996.12.29\n");
		System.out.printf("%s\t%s","모모","1996.11.09\n");
	}

	// \n, \t : 특정기능문자(이스케이프 문자)
}
